public class math {
    double fNum;
    double sNum;
    double re;

    public math() {
    }

    public math(double fNum, double sNum, double re) {
        this.fNum = fNum;
        this.sNum = sNum;
        this.re = re;
    }

    public double getfNum() {
        return fNum;
    }

    public void setfNum(double fNum) {
        this.fNum = fNum;
    }

    public double getsNum() {
        return sNum;
    }

    public void setsNum(double sNum) {
        this.sNum = sNum;
    }

    public double getRe() {
        return re;
    }

    public void setRe(double re) {
        this.re = re;
    }
    
}
